package com.organisation.pramati.webCrawler.model;

public class FileMetaData {
	
	private String authorName;
	private String fileName;
	private String dateOfMail;
	private String hyperLinkOfMail;
	private String subjectOfMail;
	
	
	
	public String getSubjectOfMail() {
		return subjectOfMail;
	}
	public void setSubjectOfMail(String subjectOfMail) {
		this.subjectOfMail = subjectOfMail;
	}
	public String getAuthorName() {
		return authorName;
	}
	public void setAuthorName(String authorName) {
		this.authorName = authorName;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public String getDateOfMail() {
		return dateOfMail;
	}
	public void setDateOfMail(String dateOfMail) {
		this.dateOfMail = dateOfMail;
	}
	public String getHyperLinkOfMail() {
		return hyperLinkOfMail;
	}
	public void setHyperLinkOfMail(String hyperLinkOfMail) {
		this.hyperLinkOfMail = hyperLinkOfMail;
	}
	
	
	

}
